-- Business Overview

-- Total Revenue
SELECT 
    SUM(quantity * vehicle_price) / 1000000 AS revenue_in_millions
FROM order_t;

-- Total orders
SELECT COUNT(DISTINCT shipper_id) AS total_orders
FROM order_t;

-- Total Customers
SELECT COUNT(DISTINCT customer_id) AS total_customers
FROM customer_t;

-- Average Rating
SELECT 
    AVG(
        CASE LOWER(customer_feedback)
            WHEN 'excellent' THEN 5
            WHEN 'good' THEN 4
            WHEN 'average' THEN 3
            WHEN 'poor' THEN 2
            WHEN 'bad' THEN 1
            ELSE NULL
        END
    ) AS average_rating
FROM order_t;

-- Last Quater revenue
SELECT 
    ROUND(SUM(quantity * vehicle_price) / 1000000, 2) AS fourth_quarter_revenue_millions
FROM order_t
WHERE QUARTER(order_date) = 4;


-- Last Quater orders
SELECT 
    COUNT(quantity * vehicle_price) AS fourth_quarter_order_sum
FROM order_t
WHERE EXTRACT(QUARTER FROM order_date) = 4;

-- Average Days to ship
SELECT 
    AVG(DATEDIFF(ship_date, order_date)) AS avg_days_to_ship
FROM order_t
WHERE ship_date IS NOT NULL;

-- Percentage of Good feedback
SELECT 
    ROUND(
        100.0 * SUM(CASE WHEN LOWER(customer_feedback) = 'good' THEN 1 ELSE 0 END) 
        / COUNT(*), 
        2
    ) AS percentage_good_feedback
FROM order_t
WHERE customer_feedback IS NOT NULL; 
