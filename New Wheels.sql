use wheels;

-- Question 1:
-- Find the total number of customers who have placed orders. What is the distribution of the customers across states?

SELECT 
	state, 
	COUNT(customer_id) AS total_customers
FROM customer_t
GROUP BY 1
ORDER BY 2 DESC;


-- Question 2:
-- Which are the top 5 vehicle makers preferred by the customers?

SELECT 
	vehicle_maker AS top_vehicle_makers, 
	COUNT(customer_id) AS total_customers
FROM product_t JOIN customer_t
GROUP BY top_vehicle_makers
ORDER BY total_customers DESC
LIMIT 5;


-- Question 3:
-- Which is the most preferred vehicle maker in each state?

SELECT *
FROM
(
	SELECT 
		state, 
		vehicle_maker,
		COUNT(customer_id) AS total_customers,
    RANK() OVER (PARTITION BY state ORDER BY COUNT(customer_id) DESC) AS ranking
    FROM product_t 
    JOIN order_t USING(product_id)
    JOIN customer_t USING(customer_id)
	GROUP BY 1, 2 
) AS preferred_vehicle
WHERE ranking = 1
ORDER BY 3 DESC;


-- Question 4:
-- Find the overall average rating given by the customers. What is the average rating in each quarter?

WITH rating AS 
(
	SELECT
		customer_feedback,
		quarter_number,
    CASE
		WHEN customer_feedback = 'very bad' THEN '1'
		WHEN customer_feedback = 'bad' THEN '2'
		WHEN customer_feedback = 'okay' THEN '3'
		WHEN customer_feedback = 'good' THEN '4'
		WHEN customer_feedback = 'very good' THEN '5'
    END AS total_rating
    FROM order_t
)
    
SELECT 
		quarter_number,
    ROUND(AVG(total_rating), 2) AS average_rating
FROM rating
GROUP BY 1 
ORDER BY 1 ASC;

-- Question 5:
-- Find the percentage distribution of feedback from the customers. Are customers getting more dissatisfied over time?

WITH cust_feed AS 
(
	SELECT 
		quarter_number,
		ROUND(SUM(CASE WHEN customer_feedback = 'very good' THEN 1 ELSE 0 END), 2) AS very_good,
		ROUND(SUM(CASE WHEN customer_feedback = 'good' THEN 1 ELSE 0 END), 2) AS good,
		ROUND(SUM(CASE WHEN customer_feedback = 'okay' THEN 1 ELSE 0 END), 2) AS okay,
		ROUND(SUM(CASE WHEN customer_feedback = 'bad' THEN 1 ELSE 0 END), 2) AS bad,
		ROUND(SUM(CASE WHEN customer_feedback = 'very bad' THEN 1 ELSE 0 END), 2) AS very_bad,
		ROUND(COUNT(customer_feedback), 2) AS total_feedback
	FROM order_t
	GROUP BY 1
    ORDER BY 1 ASC
)
   
  SELECT 
		quarter_number,
        ROUND((very_good/total_feedback), 2) AS very_good,
        ROUND((good/total_feedback), 2) AS good,
        ROUND((okay/total_feedback), 2) AS okay,
        ROUND((bad/total_feedback), 2) AS bad,
        ROUND((very_bad/total_feedback), 2) AS very_bad
	FROM cust_feed
	GROUP BY 1
    ORDER BY 1 ASC;


